# 是否是合法的标识符字符串
s = 'hello,world'
print(s.isidentifier())

# 判断字符串是否全部由空白字符串组成（回车、换行、水平制表符）
print('\t'.isspace())

# 判断字符串是否全部由字母组成
print('aaav'.isalpha())

# 判断字符串是否全部由十进制的数字组成
print('87241'.isdecimal())

# 判断字符串是否全部由数字组成
print('1542358923'.isnumeric())

# 判断字符串是否全部由字母和数字组成
print('adaa080'.isalnum())







